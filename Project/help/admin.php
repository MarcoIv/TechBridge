<div class="help-container">

    <h2>Admin Guide</h2>

    <p>Admins can manage user requests, reply to messages, and change site themes.</p>
    <h3>1. Managing Requests</h3>
    <p>To manage user requests, navigate to the "Admin Dashboard" and click on "Manage Requests". Here you can view all
        submitted requests and click on any request to view details and delete.</p>
    <h3>2. Replying to Messages</h3>
    <p>To reply to user messages, go to the "Admin Dashboard" and click on "Manage Requests". Here you can see all the
        requests and click on any request to read and reply.</p>
    <h3>3. Changing Site Themes</h3>
    <p>To change the site theme, navigate to the "Admin Dashboard" and click on "Change Theme". Select a new theme from
        the menu and click "Apply Theme" to apply the new theme to the whole site.</p>
    <h3>4. Managing Products</h3>
    <p>To manage products, navigate to the "Admin Dashboard" and click on "Manage Products". Here you can add, edit, or
        delete products from the catalogue. To add a new product, click on the "Add New Product" button then fill the
        new product details. To delete a product, navigate to the Manage Products page and click the delete button.</p>
    <h3>5. Managing Users</h3>
    <p>To manage users, navigate to the "Admin Dashboard" and click on "Manage Users". Here you can view all registered
        users and click on any user to disable their account. Disabled users cannot login.</p>
    <h3>6. Viewing Statistics</h3>
    <p>To view site statistics, navigate to the "Admin Dashboard" and click on "View Monitor". Here you can see various
        metrics and analytics about the site's performance and user engagement (requests).</p>
