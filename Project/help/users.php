<div class="help-container">

    <h2>User Guide</h2>

    <p>This guide explains how to register, log in, submit requests, and browse the catalogue.</p>

    <h3>1. Registration</h3>
    <p>To create an account, click on the "Register" button on the top right corner of the homepage. Fill in the
        required information, including your name, email address, and password. Once you submit the form, your account
        will be created.</p>
    <h3>2. Logging In</h3>
    <p>To log in, click on the "Login" button on the top right corner of the homepage. Enter your email address and
        password, then click "Submit". If your credentials are correct, you will be logged in and redirected to your
        dashboard.</p>
    <h3>3. Submitting Requests</h3>
    <p>To submit a new support request, navigate to the "Requests" section of your dashboard and click on "New Request".
        Fill in the required information, including a title, description, and optional category. Once you submit the
        form, your request will be submitted and our team will get back to you shortly.</p>
    <h3>4. Viewing Your Requests</h3>
    <p>To view your submitted requests, go to the "Requests" section of your dashboard. Click on any request to view
        more details.</p>
    <h3>5. Browsing the Catalogue</h3>
    <p>To browse the catalogue, click on the "Catalogue" link in the main navigation menu. Click on any item to view
        more details and make a request if needed.</p>
